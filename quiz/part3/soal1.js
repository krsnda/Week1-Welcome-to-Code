let angka1=0;
let angka2=5;

console.log('Ascending');
while (angka1<=5) {
    console.log('ke- ' +angka1);
    angka1++;
}

console.log('Descending');
while (angka2>=0) {
    console.log('ke- ' +angka2);
    angka2--;
}
